#include "pbdata.h"

void DS1340_I2C_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;	
	
	GPIO_InitStructure.GPIO_Pin=DS1340_I2C_SCL|DS1340_I2C_SDA;
	GPIO_InitStructure.GPIO_Speed=GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_Mode=GPIO_Mode_Out_PP;
	GPIO_Init(DS1340_GPIO_I2C,&GPIO_InitStructure);

	DS1340_I2C_SCL_H;
	DS1340_I2C_SDA_H;
}

void DS1340_I2C_SDA_OUT(void)
{
  GPIO_InitTypeDef GPIO_InitStructure;	
	
	GPIO_InitStructure.GPIO_Pin=DS1340_I2C_SDA;
	GPIO_InitStructure.GPIO_Speed=GPIO_Speed_50MHz;
	GPIO_InitStructure.GPIO_Mode=GPIO_Mode_Out_PP;
	GPIO_Init(DS1340_GPIO_I2C,&GPIO_InitStructure);
}

void DS1340_I2C_SDA_IN(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;	
	
	GPIO_InitStructure.GPIO_Pin=DS1340_I2C_SDA;
	GPIO_InitStructure.GPIO_Mode=GPIO_Mode_IPU;
	GPIO_Init(DS1340_GPIO_I2C,&GPIO_InitStructure);
}

//产生起始信号
void DS1340_I2C_Start(void)
{
  DS1340_I2C_SDA_OUT();
	
	DS1340_I2C_SDA_H;
	DS1340_I2C_SCL_H;
	delay_us(3);
	DS1340_I2C_SDA_L;
	delay_us(3);
	DS1340_I2C_SCL_L;
}

//产生停止信号
void DS1340_I2C_Stop(void)
{
   DS1340_I2C_SDA_OUT();

   DS1340_I2C_SCL_L;
   DS1340_I2C_SDA_L;
   DS1340_I2C_SCL_H;
   delay_us(3);
   DS1340_I2C_SDA_H;
   delay_us(3);
}

//主机产生应答信号ACK
void DS1340_I2C_Ack(void)
{
   DS1340_I2C_SCL_L;
   DS1340_I2C_SDA_OUT();
   DS1340_I2C_SDA_L;
   delay_us(3);
   DS1340_I2C_SCL_H;
   delay_us(3);
   DS1340_I2C_SCL_L;
}

//主机不产生应答信号NACK
void DS1340_I2C_NAck(void)
{
   DS1340_I2C_SCL_L;
   DS1340_I2C_SDA_OUT();
   DS1340_I2C_SDA_H;
   delay_us(3);
   DS1340_I2C_SCL_H;
   delay_us(3);
   DS1340_I2C_SCL_L;
}
//等待从机应答信号
//返回值：1 接收应答失败
//		  0 接收应答成功
u8 DS1340_I2C_Wait_Ack(void)
{
	u8 tempTime=0;

	DS1340_I2C_SDA_IN();

	DS1340_I2C_SDA_H;
	delay_us(3);
	DS1340_I2C_SCL_H;
	delay_us(3);

	while(GPIO_ReadInputDataBit(DS1340_GPIO_I2C,DS1340_I2C_SDA))
	{
		tempTime++;
		if(tempTime>250)
		{
			DS1340_I2C_Stop();
			return 1;
		}	 
	}

	DS1340_I2C_SCL_L;
	return 0;
}
//I2C 发送一个字节
void DS1340_I2C_Send_Byte(u8 txd)
{
	u8 i=0;

	DS1340_I2C_SDA_OUT();
	DS1340_I2C_SCL_L;//拉低时钟开始数据传输

	for(i=0;i<8;i++)
	{
		if((txd&0x80)>0) //0x80  1000 0000
			DS1340_I2C_SDA_H;
		else
			DS1340_I2C_SDA_L;

		txd<<=1;
		DS1340_I2C_SCL_H;
		delay_us(3); //发送数据
		DS1340_I2C_SCL_L;
		delay_us(3);
	}
}

//I2C 读取一个字节

u8 DS1340_I2C_Read_Byte(u8 ack)
{
   u8 i=0,receive=0;

   DS1340_I2C_SDA_IN();
   for(i=0;i<8;i++)
   {
   	DS1340_I2C_SCL_L;
		delay_us(3);
		DS1340_I2C_SCL_H;
		receive<<=1;
		if(GPIO_ReadInputDataBit(DS1340_GPIO_I2C,DS1340_I2C_SDA))
		   receive++;
		delay_us(3);	
   }

   	if(ack==0)
	   	DS1340_I2C_NAck();
	else
		DS1340_I2C_Ack();

	return receive;
}























